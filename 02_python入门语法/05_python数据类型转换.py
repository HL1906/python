"""
int(x)    将x转换成int整数类型
float(x)  将x转换成float浮点数类型
str(x)    将x转换成str字符串类型
"""



# 将数字类型转换成字符串
num_str = str(11)
print(type(num_str), num_str)

float_str = str(11.456)
print(type(float_str), float_str)

print("--------------------")
# 将字符串转换成数字
num_int = int("11")
print(type(num_int), num_int)

num_float = float("11.456")
print(type(num_float), num_float)
